        public void getSection(){
            String TchName = tcLabel.getText();



    try{
        Set<String> uniqueSections = new HashSet<>(); // Create a set to store unique sections
      
                
                PreparedStatement getICTSec = conn.prepareStatement("SELECT SecAssigned FROM ictsubjects WHERE TeacherAssigned = ?");
                getICTSec.setString(1, TchName);
                ResultSet findICTSec = getICTSec.executeQuery();

                while (findICTSec.next()) {
                    String getSecs = findICTSec.getString("SecAssigned");
                        
                       uniqueSections.add(getSecs);
                    System.out.println("Found section: " + getSecs);     
        }
                
          for (String section : uniqueSections) {
                sectionVal.addElement(section);
            }
                

       

        
    } catch (SQLException ex) {
        Logger.getLogger(TCHSECTION.class.getName()).log(Level.SEVERE, null, ex);
    }
        
    

    
    
}
        
        public void getHE(){
            String TchName = tcLabel.getText(); 
            
        try {
            Set<String> uniqueSections = new HashSet<>();
            
            PreparedStatement getHESec = conn.prepareStatement("SELECT SecAssigned FROM hesubjects WHERE TeacherAssigned = ?");
            getHESec.setString(1, TchName);
            ResultSet findHESec = getHESec.executeQuery();
            
            while (findHESec.next()) {
                String getSecs = findHESec.getString("SecAssigned");
                
                uniqueSections.add(getSecs);
                System.out.println("Found section: " + getSecs);
            }
            
            
            // Add unique sections to sectionVal
            //  // Clear existing elements
            
            for (String section : uniqueSections) {
                sectionVal.addElement(section);
            }
        } catch (SQLException ex) {
            Logger.getLogger(TCHSECTION.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        
