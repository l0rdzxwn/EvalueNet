   public void getSubs(){
        try {
            String TchName = tcLabel.getText();
            String getSecs = (String) secCB.getSelectedItem();
            
            PreparedStatement DBName = conn.prepareStatement("SELECT * FROM classlist WHERE Class_Name = ?");
                DBName.setString(1,getSecs);
                    ResultSet getDBName = DBName.executeQuery();
                        if(getDBName.next()){
                            String getStrand = getDBName.getString("STRAND");
                            String TblName = getStrand+"SUBJECTS";    
                            String ictSub = "SELECT SubjectName FROM "+TblName+" WHERE TeacherAssigned = ? AND SecAssigned = ?";
                            PreparedStatement getSub = conn.prepareStatement(ictSub);
                                getSub.setString(1, TchName);
                                getSub.setString(2, getSecs);
                            ResultSet findSub = getSub.executeQuery();
                                Set<String> uniqueSubjects = new HashSet<>();
                                    while (findSub.next()) {
                                String getSubs = findSub.getString("SubjectName");
                
                                 uniqueSubjects.add(getSubs);
                                System.out.println("Found subject: " + getSubs);
                                }
            
            // Add unique subjects to subjectVal
            subjectVal.removeAllElements(); // Clear existing elements
            for (String subject : uniqueSubjects) {
                subjectVal.addElement(subject);
                
            }
                        }
        } catch (SQLException ex) {
            Logger.getLogger(TCHSECTION.class.getName()).log(Level.SEVERE, null, ex);
        }
   
   }
        
    
