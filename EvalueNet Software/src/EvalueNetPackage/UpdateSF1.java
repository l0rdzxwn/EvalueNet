    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(studName.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Please enter a student's name!");
        }else if(studLRN.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Please enter a student's LRN!");
        }else{
            try {
                String Name = studName.getText();
                String LRN = studLRN.getText();
                
                String findStud = "SELECT * FROM sf1_db WHERE Name = ? AND LRN = ?";
                PreparedStatement pstm = conn.prepareStatement(findStud);
                pstm.setString(1, Name);
                pstm.setString(2, LRN);
                ResultSet search = pstm.executeQuery();
                
                if(search.next()){
                  JOptionPane.showMessageDialog(null,"Student found!");  
                    
                  String lrnVal = search.getString("LRN");
                  String nameVal = search.getString("NAME");
                  String sexVal = search.getString("SEX");
                  String bdayVal = search.getString("BIRTHDAY");
                  String ageVal = search.getString("AGE");
                  String mtVal = search.getString("MOTHER_TONGUE");
                  String egVal = search.getString("ETHNIC_GROUP");
                  String relVal = search.getString("RELIGION");
                  String adrsVal = search.getString("ADDRESS");
                  String fnameVal = search.getString("FATHER_NAME");
                  String mnameVal = search.getString("MOTHER_NAME");
                  String cnVal = search.getString("CONTACT_NUMBER");
                  String lmVal = search.getString("LEARNING_MODALITY");

                  String[] parts = bdayVal.split("-");
                  String getMonth = parts[0];
                  String getDay = parts[1];
                  String getYear = parts[2];
                  
                  boolean monthVal = Boolean.parseBoolean(getMonth);
                  boolean dayVal = Boolean.parseBoolean(getDay);
                  boolean yearVal = Boolean.parseBoolean(getYear);
                  
                  MONTHinfo.setSelectedItem(getMonth);
                  DAYinfo.setSelectedItem(getDay);
                  YEARinfo.setSelectedItem(getYear);
                  LRNinfo.setText(lrnVal);
                  NAMEinfo.setText(nameVal);
                  SEXinfo.setSelectedItem(sexVal);
                  AGEinfo.setSelectedItem(ageVal);
                  MTinfo.setText(mtVal);
                  EGinfo.setText(egVal);
                  RELinfo.setText(relVal);
                  ADRSinfo.setText(adrsVal);
                  FNAMEinfo.setText(fnameVal);
                  MNAMEinfo.setText(mnameVal);
                  CNinfo.setText(cnVal);
                  LMinfo.setText(lmVal);
                  
                  
                }else{
                    JOptionPane.showMessageDialog(null, "Student not found. Please check the values.");
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(UpdateSF1.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

